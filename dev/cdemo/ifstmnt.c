#include <stdio.h>

int main()
{
     int a = 0;
     int b = 2;
     // if statement to test if a is equal to b
     if (a == b)
     {
         printf("a is equal to b|n");
     }     
     else
     {
         printf("a is not equal to b\n");
     }
 
     // if statement to test if a is greater than b
     if (a > b)
     {
         printf("a is greater than b|n");
     }
     else
     {
         printf("a is not greater than b\n");
     }
 
     // if statement to test if a is greater than or equal to b
     if (a >= b)
     {
         printf("a is greater than or equal to b|n");
     }
     else
     {
         printf("a is not greater than or equal to b\n");
     }
 
     // if statement to test if a is equal to 0 and b is equal to 0
     if (a == 0 && b == 0)
     {
         printf("both a and b are not equal to 0|n");
     }
     else
     {
         printf("both a and b are not equal to 0\n");
     }
 
     // if statement to test if a is equal to 0
     if (a == 0)
     {
         printf("a is equal to 0|n");
     }
     else
     {
         printf("a is not equal to 0\n");
     }

    // if statement to test if a is equal to 0 or b is equal to 0
     if (a == 0 || b == 0)
     {
         printf("a is equal to 0 or b is equal to 0|n");
     }
     else
     {
         printf("a or b is not equal to 0\n");
     }
}
